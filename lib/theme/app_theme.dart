  /// Data-specific text styles using monospace fonts
  static TextStyle dataTextStyle(
      {required bool isLight, double fontSize = 14}) {
    return GoogleFonts.poppins(
      fontSize: fontSize,
      fontWeight: FontWeight.w500, // Medium for precise data alignment
      color: isLight ? textPrimaryLight : textPrimaryDark,
      letterSpacing: 0.0, // Monospace doesn't need additional spacing
    );
  }

  /// Price change text styles with color coding
  static TextStyle priceChangeTextStyle({
    required bool isLight,
    required bool isPositive,
    double fontSize = 14,
  }) {
    Color color;
    if (isPositive) {
      color = isLight ? positiveGreen : positiveGreenDark;
    } else {
      color = isLight ? negativeRed : negativeRedDark;
    }

    return GoogleFonts.poppins(
      fontSize: fontSize,
      fontWeight: FontWeight.w500,
      color: color,
    );
  }
